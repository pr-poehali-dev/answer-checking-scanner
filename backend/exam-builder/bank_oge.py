# ─── ХЕЛПЕР ДЛЯ КОМПАКТНЫХ БАНКОВ ───────────────────────────────────────────
def _short(num, topic, points, instruction, items):
    """Слот short + варианты в формате (q, a, e)."""
    return num, {"type": "short", "topic": topic, "points": points,
                 "instruction": instruction}, [
        {"question": q, "options": [], "answer": str(a), "explanation": e}
        for q, a, e in items
    ]


def _choice(num, topic, points, instruction, items):
    """Слот choice + варианты в формате (q, [opts], answer, e)."""
    return num, {"type": "choice", "topic": topic, "points": points,
                 "instruction": instruction, "options_count": 4}, [
        {"question": q, "options": opts, "answer": str(a), "explanation": e}
        for q, opts, a, e in items
    ]


def _multi(num, topic, points, instruction, items):
    return num, {"type": "multi", "topic": topic, "points": points,
                 "instruction": instruction}, [
        {"question": q, "options": opts, "answer": str(a), "explanation": e}
        for q, opts, a, e in items
    ]


def _long(num, topic, points, instruction, items):
    return num, {"type": "long", "topic": topic, "points": points,
                 "instruction": instruction}, [
        {"question": q, "options": [], "answer": str(a), "explanation": e}
        for q, a, e in items
    ]


def _build(slots):
    """Преобразует список слотов в формат {structure, tasks}."""
    structure = []
    tasks = {}
    for num, info, variants in slots:
        structure.append({"num": num, **info})
        tasks[num] = variants
    return {"structure": structure, "tasks": tasks}


