def merge_extras(base_bank: dict, extras_by_subject: dict) -> dict:
    """Дополняет базовый банк extra-заданиями по соответствующим номерам."""
    for subject, extras in extras_by_subject.items():
        if subject not in base_bank:
            continue
        for num, extra_list in extras.items():
            base_bank[subject]["tasks"].setdefault(num, []).extend(extra_list)
    return base_bank


